namespace Engine.OpenGL.Enums
{
	/// <summary>
	/// Represents different texture units used in OpenGL.
	/// </summary>
	public enum TextureUnit
	{
		/// <summary>
		/// Represents no specific texture unit.
		/// </summary>
		None,

		/// <summary>
		/// Represents the default texture unit used in OpenGL.
		/// </summary>
		Texture0 = 0x84C0,

		/// <summary>
		/// Represents the first texture unit used in OpenGL.
		/// </summary>
		Texture1 = 0x84C1,

		/// <summary>
		/// Represents the second texture unit in OpenGL.
		/// </summary>
		Texture2 = 0x84C2,

		/// <summary>
		/// Represents the third texture unit in OpenGL.
		/// </summary>
		Texture3 = 0x84C3,

		/// <summary>
		/// Represents the fourth texture unit in OpenGL.
		/// </summary>
		Texture4 = 0x84C4,

		/// <summary>
		/// Represents the fifth texture unit in OpenGL.
		/// </summary>
		Texture5 = 0x84C5,

		/// <summary>
		/// Represents the sixth texture unit used in OpenGL.
		/// </summary>
		Texture6 = 0x84C6,

		/// <summary>
		/// Represents the seventh texture unit in OpenGL.
		/// </summary>
		Texture7 = 0x84C7,

		/// <summary>
		/// Represents the eighth texture unit in OpenGL.
		/// </summary>
		Texture8 = 0x84C8,

		/// <summary>
		/// Represents the ninth texture unit in OpenGL.
		/// </summary>
		Texture9 = 0x84C9,
	}
}
