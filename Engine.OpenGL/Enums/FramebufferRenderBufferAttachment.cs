namespace Engine.OpenGL.Enums
{
	/// <summary>
	/// Enumerates the attachment points that can be used when binding a render buffer to a framebuffer.
	/// </summary>
	public enum FramebufferRenderBufferAttachment
	{
		/// <summary>
		/// None.
		/// </summary>
		None = 0,

		/// <summary>
		/// Color attachment point zero.
		/// </summary>
		ColorAttachment0 = 0x8CE0,

		/// <summary>
		/// Color attachment point one.
		/// </summary>
		ColorAttachment1 = 0x8CE1,

		/// <summary>
		/// Color attachment point two.
		/// </summary>
		ColorAttachment2 = 0x8CE2,

		/// <summary>
		/// Color attachment point three.
		/// </summary>
		ColorAttachment3 = 0x8CE3,

		/// <summary>
		/// Color attachment point four.
		/// </summary>
		ColorAttachment4 = 0x8CE4,

		/// <summary>
		/// Color attachment point five.
		/// </summary>
		ColorAttachment5 = 0x8CE5,

		/// <summary>
		/// Color attachment point six.
		/// </summary>
		ColorAttachment6 = 0x8CE6,

		/// <summary>
		/// Color attachment point size.
		/// </summary>
		ColorAttachment7 = 0x8CE7,

		/// <summary>
		/// Color attachment point eight.
		/// </summary>
		ColorAttachment8 = 0x8CE8,

		/// <summary>
		/// Color attachment point nine.
		/// </summary>
		ColorAttachment9 = 0x8CE9,

		/// <summary>
		/// Color attachment point ten.
		/// </summary>
		ColorAttachment10 = 0x8CEA,

		/// <summary>
		/// Color attachment point eleven.
		/// </summary>
		ColorAttachment11 = 0x8CEB,

		/// <summary>
		/// Color attachment point twelve.
		/// </summary>
		ColorAttachment12 = 0x8CEC,

		/// <summary>
		/// Color attachment point thirteen.
		/// </summary>
		ColorAttachment13 = 0x8CED,

		/// <summary>
		/// Color attachment point fourteen.
		/// </summary>
		ColorAttachment14 = 0x8CEE,

		/// <summary>
		/// Color attachment point fifteen.
		/// </summary>
		ColorAttachment15 = 0x8CEF,

		/// <summary>
		/// Specifies the depth attachment point for binding a render buffer to a framebuffer.
		/// </summary>
		DepthAttachment = 0x8D00,

		/// <summary>
		/// Specifies the stencil attachment point for binding a render buffer to a framebuffer.
		/// </summary>
		StencilAttachment = 0x8D20,
	}
}
