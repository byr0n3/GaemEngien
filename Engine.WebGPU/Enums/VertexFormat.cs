namespace wgpu.Enums
{
	/// <summary>
	/// Enumeration representing the formats for vertex attributes.
	/// </summary>
	public enum VertexFormat
	{
		/// <summary>
		/// Represents a vertex format with 8-bit unsigned integer components.
		/// </summary>
		Uint8 = 1,

		/// <summary>
		/// Represents a vertex format with two 8-bit unsigned integer components.
		/// </summary>
		Uint8x2,

		/// <summary>
		/// Represents a vertex format with four 8-bit unsigned integer components.
		/// </summary>
		Uint8x4,

		/// <summary>
		/// Represents a vertex format with 8-bit signed integer components.
		/// </summary>
		Sint8,

		/// <summary>
		/// Represents a vertex format with two 8-bit signed integer components.
		/// </summary>
		Sint8x2,

		/// <summary>
		/// Represents a vertex format with 4 components of 8-bit signed integer.
		/// </summary>
		Sint8x4,

		/// <summary>
		/// Represents a vertex format with 8-bit unsigned normalized integer components.
		/// </summary>
		Unorm8,

		/// <summary>
		/// Represents a vertex format with two 8-bit unsigned normalized integer components.
		/// </summary>
		Unorm8x2,

		/// <summary>
		/// Represents a vertex format with 4 components, each being an unsigned normalized 8-bit integer.
		/// </summary>
		Unorm8x4,

		/// <summary>
		/// Represents a vertex format with 8-bit signed normalized integer components.
		/// </summary>
		Snorm8,

		/// <summary>
		/// Represents a vertex format with 8-bit signed normalized integer components.
		/// </summary>
		Snorm8x2,

		/// <summary>
		/// Represents a vertex format with 4 signed normalized 8-bit integer components.
		/// </summary>
		Snorm8x4,

		/// <summary>
		/// Represents a vertex format with 16-bit unsigned integer components.
		/// </summary>
		Uint16,

		/// <summary>
		/// Represents a vertex format with two 16-bit unsigned integer components.
		/// </summary>
		Uint16x2,

		/// <summary>
		/// Represents a vertex format with four 16-bit unsigned integer components.
		/// </summary>
		Uint16x4,

		/// <summary>
		/// Represents a vertex format with 16-bit signed integer components.
		/// </summary>
		Sint16,

		/// <summary>
		/// Represents a vertex format with 2 components of signed 16-bit integer.
		/// </summary>
		Sint16x2,

		/// <summary>
		/// Represents a vertex format with four 16-bit signed integer components.
		/// </summary>
		Sint16x4,

		/// <summary>
		/// Represents a vertex format with 16-bit unsigned normalized integer components.
		/// </summary>
		Unorm16,

		/// <summary>
		/// Represents a vertex format with two 16-bit unsigned normalized integer components.
		/// </summary>
		Unorm16x2,

		/// <summary>
		/// Represents a vertex format with 16-bit unsigned normalized integer components, with four channels.
		/// </summary>
		Unorm16x4,

		/// <summary>
		/// Represents a vertex format with 16-bit signed normalized integer components.
		/// </summary>
		Snorm16,

		/// <summary>
		/// Represents a vertex format with two 16-bit signed normalized integer components.
		/// </summary>
		Snorm16x2,

		/// <summary>
		/// Represents a vertex format with four 16-bit signed normalized integer components.
		/// </summary>
		Snorm16x4,

		/// <summary>
		/// Represents a vertex format with 16-bit floating-point components.
		/// </summary>
		Float16,

		/// <summary>
		/// Represents a vertex format with two 16-bit floating-point components.
		/// </summary>
		Float16x2,

		/// <summary>
		/// Represents a vertex format with four 16-bit floating-point components.
		/// </summary>
		Float16x4,

		/// <summary>
		/// Represents a vertex format with 32-bit floating point components.
		/// </summary>
		Float32,

		/// <summary>
		/// Represents a vertex format with two 32-bit floating-point components.
		/// </summary>
		Float32x2,

		/// <summary>
		/// Represents a vertex format with three 32-bit floating-point components.
		/// </summary>
		Float32x3,

		/// <summary>
		/// Represents a vertex format with 4 components of 32-bit floating-point values.
		/// </summary>
		Float32x4,

		/// <summary>
		/// Represents a vertex format with 32-bit unsigned integer components.
		/// </summary>
		Uint32,

		/// <summary>
		/// Represents a vertex format with two 32-bit unsigned integer components.
		/// </summary>
		Uint32x2,

		/// <summary>
		/// Represents a vertex format with three 32-bit unsigned integer components.
		/// </summary>
		Uint32x3,

		/// <summary>
		/// Represents a vertex format with 4 components, each being an unsigned 32-bit integer.
		/// </summary>
		Uint32x4,

		/// <summary>
		/// Represents a vertex format with 32-bit signed integer components.
		/// </summary>
		Sint32,

		/// <summary>
		/// Represents a vertex format with two 32-bit signed integer components.
		/// </summary>
		Sint32x2,

		/// <summary>
		/// Represents a vertex format with three 32-bit signed integer components.
		/// </summary>
		Sint32x3,

		/// <summary>
		/// Represents a vertex format with four 32-bit signed integer components.
		/// </summary>
		Sint32x4,

		/// <summary>
		/// Represents a vertex format with three 10-bit unsigned normalized integer components and one 2-bit unsigned normalized integer component.
		/// </summary>
		Unorm10_10_10_2,

		/// <summary>
		/// Represents a vertex format with 8-bit unsigned normalized integer components in the BGRA order.
		/// </summary>
		Unorm8x4BGRA,
	}
}
