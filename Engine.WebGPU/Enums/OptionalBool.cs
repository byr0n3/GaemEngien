namespace wgpu.Enums
{
	/// <summary>
	/// Represents an optional boolean value that can be either true, false, or undefined.
	/// </summary>
	public enum OptionalBool
	{
		/// <summary>
		/// Specifies that the optional boolean value is false.
		/// </summary>
		False = 0,

		/// <summary>
		/// Specifies that the optional boolean value is true.
		/// </summary>
		True,

		/// <summary>
		/// Specifies that the optional boolean value is undefined.
		/// </summary>
		Undefined,
	}
}
